"""A stack in python."""


class Stack(LinkedList):
    """A stack data structure."""

    def __init__(self, iterable=None):
        """Init Stack."""

    def __len__(self):
        """Return the size of the stack."""


    def push(self, value):
        """Add a value to the top of stack."""

    def pop(self):
        """Remove value from top of stack and return it."""
